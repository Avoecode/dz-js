
let lang = 'es';

switch (lang) {
    case 'en':
        console.log('Hello!');
        break
    case 'ru':
        console.log('Здравствуйте!');
        break
    case 'de':
        console.log('Hallo!');
        break
    case 'fr':
        console.log('Bonjour!');
        break
    case 'es':
        console.log('¡Hola!');
        break
    default:
        console.log('Язык не поддерживается!');
}


